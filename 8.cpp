//program to display the size of different datatypes
#include <iostream>
using namespace std;
int main(){
	cout << "The size of int datatype : "<< sizeof(int)<< "bytes"<<endl;
	cout << "The size of char datatype : "<< sizeof(char)<< "bytes"<<endl;
	cout << "The size of float datatype : "<< sizeof(float)<< "bytes"<<endl;
	cout << "The size of long datatype : "<< sizeof(long)<< "bytes"<<endl;
	cout << "The size of short datatype : "<< sizeof(short)<< "bytes"<<endl;
	cout << "The size of double datatype : "<< sizeof(double)<< "bytes"<<endl;
	cout << "The size of string datatype : "<< sizeof(string)<< "bytes"<<endl;
	cout << "The size of bool datatype : "<< sizeof(bool)<< "bytes"<<endl;
}
